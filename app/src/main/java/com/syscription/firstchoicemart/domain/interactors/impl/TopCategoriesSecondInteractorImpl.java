package com.syscription.firstchoicemart.domain.interactors.impl;

import android.util.Log;

import com.syscription.firstchoicemart.Network.ApiClient;
import com.syscription.firstchoicemart.Network.response.CategoryResponse;
import com.syscription.firstchoicemart.Network.services.TopCategoryApiInterface;
import com.syscription.firstchoicemart.domain.executor.Executor;
import com.syscription.firstchoicemart.domain.executor.MainThread;
import com.syscription.firstchoicemart.domain.interactors.TopCategoryInteractor;
import com.syscription.firstchoicemart.domain.interactors.TopSecondCategoryInteractor;
import com.syscription.firstchoicemart.domain.interactors.base.AbstractInteractor;

import retrofit2.Call;
import retrofit2.Callback;
import retrofit2.Response;

public class TopCategoriesSecondInteractorImpl extends AbstractInteractor {

    private final TopSecondCategoryInteractor.CallBack mCallback;
    private TopCategoryApiInterface apiService;

    public TopCategoriesSecondInteractorImpl(Executor threadExecutor, MainThread mainThread, TopSecondCategoryInteractor.CallBack callBack) {
        super(threadExecutor, mainThread);
        mCallback = callBack;
    }

    @Override
    public void run() {
        apiService = ApiClient.getClient().create(TopCategoryApiInterface.class);
        Call<CategoryResponse> call = apiService.getTopSecondPageCategories();

        call.enqueue(new Callback<CategoryResponse>() {
            @Override
            public void onResponse(Call<CategoryResponse> call, Response<CategoryResponse> response) {
                try {
                    mCallback.onTopSecondCategoriesDownloaded(response.body().getData());
                } catch (Exception e) {
                    Log.e("Exception", e.getMessage());
                }
            }

            @Override
            public void onFailure(Call<CategoryResponse> call, Throwable t) {
                mCallback.onTopSecondCategoriesDownloadError();
            }
        });

    }
}
